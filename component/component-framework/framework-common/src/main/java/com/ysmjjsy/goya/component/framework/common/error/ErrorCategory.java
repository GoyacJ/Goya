package com.ysmjjsy.goya.component.framework.common.error;

/**
 * <p>错误分类枚举，用于对错误进行稳定、可治理的归类</p>
 * <p>该枚举面向全平台（单体/微服务）统一使用，刻意不包含任何 HTTP 语义。
 * HTTP 状态码的映射应在更高层（例如 {@code framework-core}）完成。</p>
 *
 * <h2>设计说明</h2>
 * <ul>
 *   <li>分类用于制定处理策略：日志级别、告警策略、是否可重试、熔断降级、前端展示等。</li>
 *   <li>分类数量应保持克制且长期稳定；细粒度差异建议使用异常 {@code metadata} 承载。</li>
 * </ul>
 *
 * @author goya
 * @since 2026/1/24 13:20
 */
public enum ErrorCategory {

    /**
     * 参数校验/约束校验失败，例如：字段缺失、格式不合法、长度超限等。
     */
    VALIDATION,

    /**
     * 认证/鉴权失败，例如：未登录、Token 无效、权限不足等。
     */
    AUTH,

    /**
     * 业务规则冲突/业务校验失败，例如：余额不足、状态不允许、规则不满足等。
     */
    BIZ,

    /**
     * 冲突类错误，例如：幂等冲突、乐观锁冲突、并发写入冲突等。
     */
    CONFLICT,

    /**
     * 资源不存在，例如：查询的对象不存在、文件不存在等。
     */
    NOT_FOUND,

    /**
     * 限流/配额类错误，例如：超过 QPS、超过配额等。
     */
    RATE_LIMIT,

    /**
     * 远程依赖错误，例如：下游服务异常、第三方接口失败、RPC 调用失败等。
     */
    REMOTE,

    /**
     * 基础设施错误，例如：数据库连接、IO、缓存、文件系统、消息队列等问题。
     */
    INFRA,

    /**
     * 系统错误/未知错误，通常表示非预期内部故障或无法归类的错误。
     */
    SYSTEM
}
